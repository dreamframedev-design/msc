-- 2. Create File Requests Table
CREATE TABLE IF NOT EXISTS public.file_requests (
    id UUID PRIMARY KEY DEFAULT uuid_generate_v4(),
    client_tag TEXT NOT NULL,
    message TEXT,
    status TEXT DEFAULT 'pending',
    created_by UUID REFERENCES auth.users(id),
    created_at TIMESTAMPTZ DEFAULT NOW()
);

-- Enable RLS for file_requests
ALTER TABLE public.file_requests ENABLE ROW LEVEL SECURITY;

-- Policy: Admins can manage all file requests
CREATE POLICY "Admins can manage all file requests" 
ON public.file_requests FOR ALL 
USING (
  (auth.jwt() -> 'user_metadata' ->> 'role') = 'admin' OR 
  (auth.jwt() -> 'app_metadata' ->> 'role') = 'admin'
);

-- Policy: Anyone can SELECT a file request (for the magic link)
CREATE POLICY "Anyone can view a specific file request" 
ON public.file_requests FOR SELECT 
USING (true);

-- Policy: Anyone can UPDATE a file request status (when fulfilled)
CREATE POLICY "Anyone can update a file request status" 
ON public.file_requests FOR UPDATE 
USING (true);

-- ------------------------------------------

-- 3. Storage Policy for Client-Vault (Allow public upload to /requests/ folder)
-- Since we are allowing anyone with the link to upload to the "requests" folder, we need an INSERT policy
CREATE POLICY "Allow public uploads to requests folder"
ON storage.objects FOR INSERT
TO public
WITH CHECK (
  bucket_id = 'client-vault' 
  AND (storage.foldername(name))[2] = 'requests'
);

-- Allow public to insert into vault_files table for requests
CREATE POLICY "Allow public inserts for requested vault_files" 
ON public.vault_files FOR INSERT 
TO public
WITH CHECK (
  folder = 'Requested Files'
);
