-- 1. Create Ticket Comments Table
CREATE TABLE IF NOT EXISTS public.ticket_comments (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    ticket_id UUID NOT NULL REFERENCES public.client_tickets(id) ON DELETE CASCADE,
    user_id UUID NOT NULL REFERENCES auth.users(id) ON DELETE CASCADE,
    content TEXT NOT NULL,
    created_at TIMESTAMPTZ DEFAULT NOW()
);

-- Enable RLS for ticket_comments
ALTER TABLE public.ticket_comments ENABLE ROW LEVEL SECURITY;

-- Policy: Admins can see and create all comments
CREATE POLICY "Admins can view all comments" 
ON public.ticket_comments FOR SELECT 
USING (
  (auth.jwt() -> 'user_metadata' ->> 'role') = 'admin' OR 
  (auth.jwt() -> 'app_metadata' ->> 'role') = 'admin'
);

CREATE POLICY "Admins can insert comments" 
ON public.ticket_comments FOR INSERT 
WITH CHECK (
  (auth.jwt() -> 'user_metadata' ->> 'role') = 'admin' OR 
  (auth.jwt() -> 'app_metadata' ->> 'role') = 'admin'
);

-- Policy: Clients can see comments on their own tickets
CREATE POLICY "Clients can view comments on their tickets" 
ON public.ticket_comments FOR SELECT 
USING (
  ticket_id IN (
    SELECT id FROM public.client_tickets WHERE client_id = auth.uid()
  )
);

-- Policy: Clients can insert comments on their own tickets
CREATE POLICY "Clients can insert comments on their tickets" 
ON public.ticket_comments FOR INSERT 
WITH CHECK (
  ticket_id IN (
    SELECT id FROM public.client_tickets WHERE client_id = auth.uid()
  )
  AND auth.uid() = user_id
);

